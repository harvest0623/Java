package com.hh.loop;

public class WhileDemo04
{
    public static void main(String[] args)
    {
        //目标:认识while循环写法
        testWhile();
    }

    public static void testWhile()
    {
        int i = 1;
        while (i <= 5)
        {
            System.out.println("HelloWorld");
            i++;
        }
    }
}
